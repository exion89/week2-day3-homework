from math import pi


def h_area(l, w):
    '''
        Function to calculate area of a house, apartment, or other piece of land/property. 
        ...or anything on a 2d plane really
        l is length and w is width of rectangle or other shape with 4 sides
    '''
    return l * w



def c_circ(r):
    '''
    Function to calculate circumference of a circle
    r is known radius of circle
    '''
    return (2 * pi * r)



def a_circ(r):
    '''
    Bonus function for area of a circle
    r is known radius of circle
    '''
    return (pi * r ** 2)


